#include "libft.h"

char	*ft_strnstr(const char *big, const char *lil, size_t len)
{
	size_t	i;
	size_t	j;

	if (!lil[0])
		return ((char *)big);
	i = 0;
	while (big[i] != '\0' && i < len)
	{
		j = 0;
		while (big[i + j] && lil[j] && i + j < len && big[i + j] == lil[j])
			j++;
		if (!lil[j])
			return ((char*)(big + i));
		i++;
	}
	return (NULL);
}
